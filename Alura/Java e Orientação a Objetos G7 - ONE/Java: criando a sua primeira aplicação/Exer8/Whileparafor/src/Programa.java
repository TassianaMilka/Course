
//While

public class Programa {

    public static void main(String[] args) {

        int contador = 1;

        while(contador <= 10) {
            System.out.println(contador);
            contador++;
        }
    }
}

//to


//For


public class Programa {
    public static void main(String[] args) {
        for(int contador = 1; contador <= 10; contador++) {
            System.out.println(contador);
        }
    }
}