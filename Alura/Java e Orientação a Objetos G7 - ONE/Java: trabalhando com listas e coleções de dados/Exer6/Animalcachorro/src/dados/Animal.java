package dados;

public class Animal {

    private String nome;
    private String raca;
    private int ano;


    public String getNome() {
        return nome;
    }

    public String getRaca() {
        return raca;
    }

    public int getAno() {
        return ano;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public void setRaca(String raca) {
        this.raca = raca;
    }

    public void setAno(int ano) {
        this.ano = ano;
    }
}
